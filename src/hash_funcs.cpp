#include "include/hash.h"


//----------------------------------------------------------------------------------------------------------------------------------------------------//
size_t WordLengthHash(const char* word) {
    size_t hash = strlen(word);
    return hash;
}

//----------------------------------------------------------------------------------------------------------------------------------------------------//

size_t AsciiSumHash(elem_t word) {
    size_t hash = 0;
    for (size_t i = 0; i < strlen(word); i++) {
        hash += word[i];
    }
    return hash;
}

